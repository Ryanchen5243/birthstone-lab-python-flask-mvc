def get_birthstone(month):
    mon = month.lower()
    if mon == "january":
        return "Garnet"
    elif mon == "february":
        return "Amethyst"
    elif mon == "march": 
        return "Aquamarine"
    elif mon == "april":
        return "Diamond"
    elif mon == "may":
        return "Emerald"
    elif mon == "june":
        return "Pearl"
    elif mon == "july":
        return "Ruby"
    elif mon == "august":
        return "Peridot"
    elif mon == "september":
        return "Sapphire"
    elif mon == "october":
        return "Opal"
    elif mon == "november":
        return "Citrine"
    elif mon == "december":
        return "Topoz"